package com.winnie.chapter2;

public class EightDataTypeDemo {
    public static void main(String[] args){
        //基本数据类型
        byte b;// 1个字节8位  -128（负2的7次方）—（2的7次方减1）127
        short s;//2个字节16位   -32,768（负2的15次方）— （2的15次方减1）32,767
        int i; //4个字节32位   （负2的31次方）— （2的31次方减1）
        long l; // 8个字节64位    （负2的63次方）— （2的63次方减1）
        float f;//单精度浮点数  4个字节32位
        double d;//双精度浮点数  8个字节64位
        boolean bool;//布尔值 true false
        char c;//2个字节位  0—65,535（2的16次方减1）
    }
}
